# Define uma variável 'palavra' com o valor "Data Science"
palavra = "Data Science"

# Itera sobre cada caractere na string 'palavra'
for letra in palavra:
    # Verifica se o caractere atual é igual a "c"
    if(letra == "c"):
        # Se a condição for verdadeira, imprime "Achou!"
        print("Achou!")
    else:
        # Se a condição for falsa, imprime "Não achou"
        print("Não achou")
